//古典问题（兔子生崽）：有一对兔子，从出生后第3个月起每个月都生一对兔子，
//小兔子长到第三个月后每个月又生一对兔子，假如兔子都不死，问每个月的兔子总对数为多少？（输出前40个月即可）

#include <stdio.h>

int main()
{
    //硬写太难了，经分析发现兔子总对数是斐波那契数列
    int arr[40] = { 1,1 };
    int i;
    for (i = 0; i < 38; i++)
    {
        arr[i + 2] = arr[i] + arr[i + 1];
    }
    for (i = 0; i < 40; i++)
    {
        printf("%d,", arr[i]);
    }
    return 0;
}
