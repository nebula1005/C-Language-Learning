//给一个不多于5位的正整数，要求：一、求它是几位数，二、逆序打印出各位数字。

#include <stdio.h>

int main()
{
	int n, a, b, c, d, e;
	printf("请输入一个五位数：");
	scanf("%d", &n);
	e = n % 10;  //个位
	d = n % 100 / 10;  //十位
	c = n % 1000 / 100;  //百位
	b = n % 10000 / 1000;  //千位
	a = n % 100000 / 10000;  //万位
	if (a == e && b == d)
	{
		printf("是回文数。");
	}
	else
	{
		printf("不是回文数。");
	}
}
